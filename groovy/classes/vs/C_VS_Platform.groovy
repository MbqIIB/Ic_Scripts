package vs


/**
 * Esta clase representa la información relevante para una plataforma de 
 * construcción de un entregable.
 */
class C_VS_Platform {

	private String platId;
	private String platSubId;
	private String rutatar;
	public String getPlatId() {
		return platId;
	}
	public void setPlatId(String platId) {
		this.platId = platId;
	}
	public String getRutatar() {
		return rutatar;
	}
	public void setRutatar(String rutatar) {
		this.rutatar = rutatar;
	}
	public String getPlatSubId() {
		return platSubId;
	}
	public void setPlatSubId(String platSubId) {
		this.platSubId = platSubId;
	}
	
}
